package chap_02;

public class _03_Operator3 {
    public static void main(String[] args) {
        if (5 != 2) {
            System.out.println("True");
        }
        else {
            System.out.println("False");
        }

        System.out.println(5 != 5);
        System.out.println(5 != 3);
    }
}
